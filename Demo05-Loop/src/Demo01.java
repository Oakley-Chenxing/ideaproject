public class Demo01 {
    public static void main(String[] args) {
        // 输出 1-10 之间的值
        // 循环变量初始化
        // while循环变量的命名通常为 n / m
        int n = 8;
        while(n <= 10){
            // \t 表示水平制表符 表示一个缩进 Tab
            System.out.print(n +"\t");
            n++;
        }
        // 场景化，相当于一个不会做饭 有点2 的厨子
        // 打鸡蛋（搅拌鸡蛋），气泡了 下锅
        // while （没气泡）{
        // 搅拌;}
    }
}